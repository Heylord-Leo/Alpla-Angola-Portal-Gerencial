// ─── Cache types ───

interface CachedEntry {
    data: CalendarResponse;
    fetchedAt: Date;
}

function buildCacheKey(viewMode: ViewMode, from: Date, to: Date): string {
    return `${viewMode}_${formatQueryDate(from)}_${formatQueryDate(to)}`;
}

function formatTimestamp(d: Date): string {
    return d.toLocaleString('pt-AO', {
        day: '2-digit', month: '2-digit', year: 'numeric',
        hour: '2-digit', minute: '2-digit', second: '2-digit',
    });
}

// ─── Global Cache (survives route navigation) ───
const globalCalendarCache = new Map<string, CachedEntry>();

// ─── Component ───

export default function HRAttendanceCalendar() {
    const [calendarData, setCalendarData] = useState<CalendarResponse | null>(null);
    const [loading, setLoading] = useState(true);
    const [error, setError] = useState<string | null>(null);
    const [viewMode, setViewMode] = useState<ViewMode>('month');
    const [currentDate, setCurrentDate] = useState(new Date());
    const [isScrolled, setIsScrolled] = useState(false);
    const [isScrolledY, setIsScrolledY] = useState(false);
    const [showScrollHint, setShowScrollHint] = useState(false);
    const [currentPage, setCurrentPage] = useState(1);
    const pageSize = 15;
    const [lastUpdatedAt, setLastUpdatedAt] = useState<Date | null>(null);
    const [isHelpModalOpen, setIsHelpModalOpen] = useState(false);

    // Filter state
    const [filterPlant, setFilterPlant] = useState<string>('');
    const [filterDepartment, setFilterDepartment] = useState<string>('');
    const [filterCompany, setFilterCompany] = useState<string>('');
    const [filterStatus, setFilterStatus] = useState<string>('');
    const [searchQuery, setSearchQuery] = useState<string>('');

    // Detail drawer state
    const [drawerOpen, setDrawerOpen] = useState(false);
    const [drawerEmployee, setDrawerEmployee] = useState<AttendanceEmployee | null>(null);
    const [drawerDate, setDrawerDate] = useState<Date | null>(null);
    const [drawerDetail, setDrawerDetail] = useState<DayDetail | null>(null);
    const [drawerLoading, setDrawerLoading] = useState(false);
    const [drawerError, setDrawerError] = useState<string | null>(null);

    const scrollRef = useRef<HTMLDivElement>(null);
    const today = new Date();

    // ─── Date range ───

    const getDateRange = useCallback((): { from: Date; to: Date } => {
        if (viewMode === 'week') {
            const monday = getMonday(currentDate);
            return { from: monday, to: addDays(monday, 6) };
        }
        const year = currentDate.getFullYear();
        const month = currentDate.getMonth();
        return { from: new Date(year, month, 1), to: new Date(year, month + 1, 0) };
    }, [currentDate, viewMode]);

    const days = useMemo((): Date[] => {
        const { from, to } = getDateRange();
        const result: Date[] = [];
        const d = new Date(from);
        while (d <= to) {
            result.push(new Date(d));
            d.setDate(d.getDate() + 1);
        }
        return result;
    }, [getDateRange]);

    // ─── Data grouping (employee×date lookup) ───

    const attendanceLookup = useMemo(() => {
        if (!calendarData?.data) return new Map<string, AttendanceDaySummary>();
        const map = new Map<string, AttendanceDaySummary>();
        for (const row of calendarData.data) {
            const dateStr = row.date.split('T')[0];
            map.set(`${row.innuxEmployeeId}_${dateStr}`, row);
        }
        return map;
    }, [calendarData]);

    // ─── Data loading (with global in-memory cache) ───

    const loadCalendar = useCallback((forceRefresh = false) => {
        const { from, to } = getDateRange();
        const key = buildCacheKey(viewMode, from, to);

        // Serve from cache if available and not forcing refresh
        if (!forceRefresh && globalCalendarCache.has(key)) {
            const cached = globalCalendarCache.get(key)!;
            setCalendarData(cached.data);
            setLastUpdatedAt(cached.fetchedAt);
            setCurrentPage(1);
            setLoading(false);
            setError(null);
            return;
        }

        setLoading(true);
        setError(null);
        api.hrAttendance
            .getCalendar({ startDate: formatQueryDate(from), endDate: formatQueryDate(to) })
            .then((res: CalendarResponse) => {
                const now = new Date();
                globalCalendarCache.set(key, { data: res, fetchedAt: now });
                setCalendarData(res);
                setLastUpdatedAt(now);
                setCurrentPage(1);
                setLoading(false);
            })
            .catch((err: any) => {
                console.error('Attendance calendar error:', err);
                setError(err?.message || 'Falha ao carregar dados de presença.');
                setLoading(false);
            });
    }, [getDateRange, viewMode]);

    useEffect(() => { loadCalendar(); }, [loadCalendar]);

    const handleForceRefresh = useCallback(() => {
        loadCalendar(true);
    }, [loadCalendar]);

    // ─── Scroll tracking ───

    useEffect(() => {
        const el = scrollRef.current;
        if (!el) return;
        const handleScroll = () => {
            setIsScrolled(el.scrollLeft > 2);
            setIsScrolledY(el.scrollTop > 2);
            setShowScrollHint(el.scrollWidth > el.clientWidth && (el.scrollLeft + el.clientWidth) < el.scrollWidth - 4);
        };
        handleScroll();
        el.addEventListener('scroll', handleScroll, { passive: true });
        const obs = new ResizeObserver(handleScroll);
        obs.observe(el);
        return () => { el.removeEventListener('scroll', handleScroll); obs.disconnect(); };
    }, [calendarData]);


    // ─── Navigation ───

    const navPrev = () => {
        if (viewMode === 'week') setCurrentDate(addDays(currentDate, -7));
        else setCurrentDate(new Date(currentDate.getFullYear(), currentDate.getMonth() - 1, 1));
    };

    const navNext = () => {
        if (viewMode === 'week') setCurrentDate(addDays(currentDate, 7));
        else setCurrentDate(new Date(currentDate.getFullYear(), currentDate.getMonth() + 1, 1));
    };

    const navToday = () => setCurrentDate(new Date());

    // ─── Labels ───

    const getNavLabel = (): string => {
        if (viewMode === 'week') {
            const { from, to } = getDateRange();
            const fmtD = (d: Date) => d.toLocaleDateString('pt-AO', { day: 'numeric', month: 'short' });
            return `${fmtD(from)} – ${fmtD(to)} ${to.getFullYear()}`;
        }
        return currentDate.toLocaleString('pt-AO', { month: 'long', year: 'numeric' });
    };

    const getWeekBadge = (): string | null => {
        if (viewMode !== 'week') return null;
        return `Sem. ${getISOWeek(currentDate)}`;
    };

    const getScopeHeader = (): { title: string; subtitle: string } => {
        const scope = calendarData?.scopeType || 'self';
        if (scope === 'self') {
            return { title: 'Meu Registo de Presenças', subtitle: 'O seu registo individual de assiduidade.' };
        }
        if (scope === 'all') {
            return { title: 'Presenças da Equipa', subtitle: 'Visão completa — todos os funcionários.' };
        }
        if (scope === 'hr') {
            return { title: 'Presenças da Equipa', subtitle: 'Escopo R.H. — funcionários mapeados.' };
        }
        return { title: 'Presenças da Equipa', subtitle: 'Funcionários do seu departamento.' };
    };

    const getScopeDescription = (): string => {
        const scope = calendarData?.scopeType || 'self';
        switch (scope) {
            case 'all': return 'Visão completa — todos os funcionários.';
            case 'hr': return 'Escopo R.H.';
            case 'department': return 'Escopo departamental.';
            case 'self': return 'Visão pessoal.';
            default: return '';
        }
    };

    // ─── Cell interaction ───

    const handleCellClick = (employee: AttendanceEmployee, day: Date) => {
        setDrawerEmployee(employee);
        setDrawerDate(day);
        setDrawerDetail(null);
        setDrawerError(null);
        setDrawerOpen(true);
        setDrawerLoading(true);

        api.hrAttendance
            .getDayDetail(employee.innuxEmployeeId, formatQueryDate(day))
            .then((detail: DayDetail) => {
                setDrawerDetail(detail);
                setDrawerLoading(false);
            })
            .catch((err: any) => {
                if (err?.status === 404) {
                    setDrawerDetail(null);
                    setDrawerLoading(false);
                } else {
                    setDrawerError(err?.message || 'Falha ao carregar detalhe.');
                    setDrawerLoading(false);
                }
            });
    };

    const closeDrawer = () => {
        setDrawerOpen(false);
        setDrawerEmployee(null);
        setDrawerDate(null);
        setDrawerDetail(null);
    };

    const navigateDrawer = (direction: 'prev' | 'next') => {
        if (!drawerEmployee || !drawerDate) return;
        const currentIdx = days.findIndex(d => isSameDay(d, drawerDate));
        if (currentIdx < 0) return;
        const nextIdx = direction === 'prev' ? currentIdx - 1 : currentIdx + 1;
        if (nextIdx < 0 || nextIdx >= days.length) return;
        handleCellClick(drawerEmployee, days[nextIdx]);
    };

    const drawerDayIndex = drawerDate ? days.findIndex(d => isSameDay(d, drawerDate)) : -1;
    const canDrawerPrev = drawerDayIndex > 0;
    const canDrawerNext = drawerDayIndex >= 0 && drawerDayIndex < days.length - 1;

    // ─── Derived values ───

    const scope = getScopeHeader();
    const weekBadge = getWeekBadge();
    const dayColWidth = viewMode === 'week' ? 80 : 40;
    const allEmployees = calendarData?.employees || [];

    // Filter option lists (derived from loaded data)
    const filterOptions = useMemo(() => {
        const plants = new Set<string>();
        const departments = new Set<string>();
        const companies = new Set<string>();
        for (const emp of allEmployees) {
            if (emp.plantName) plants.add(emp.plantName);
            if (emp.department) departments.add(emp.department);
            if (emp.companyName) companies.add(emp.companyName);
        }
        // Collect unique statuses from attendance data
        const statuses = new Set<string>();
        if (calendarData?.data) {
            for (const row of calendarData.data) {
                if (row.attendanceStatus) statuses.add(row.attendanceStatus);
            }
        }
        // Sort statuses by their Portuguese labels
        const sortedStatuses = [...statuses].sort((a, b) => {
            const la = getStatusConfig(a).label;
            const lb = getStatusConfig(b).label;
            return la.localeCompare(lb, 'pt-AO');
        });
        return {
            plants: [...plants].sort((a, b) => a.localeCompare(b, 'pt-AO')),
            departments: [...departments].sort((a, b) => a.localeCompare(b, 'pt-AO')),
            companies: [...companies].sort((a, b) => a.localeCompare(b, 'pt-AO')),
            statuses: sortedStatuses,
        };
    }, [allEmployees, calendarData]);

    const hasActiveFilters = !!(filterPlant || filterDepartment || filterCompany || filterStatus || searchQuery.trim());

    // Filtered → sorted → paginated pipeline
    // Build a set of employee IDs that have at least one cell matching the selected status
    const employeesWithStatus = useMemo(() => {
        if (!filterStatus || !calendarData?.data) return null;
        const ids = new Set<number>();
        for (const row of calendarData.data) {
            if (row.attendanceStatus === filterStatus) ids.add(row.innuxEmployeeId);
        }
        return ids;
    }, [filterStatus, calendarData]);

    const totalEmployees = useMemo(() => {
        let list = allEmployees;
        if (filterPlant) list = list.filter(e => e.plantName === filterPlant);
        if (filterDepartment) list = list.filter(e => e.department === filterDepartment);
        if (filterCompany) list = list.filter(e => e.companyName === filterCompany);
        if (employeesWithStatus) list = list.filter(e => employeesWithStatus.has(e.innuxEmployeeId));
        if (searchQuery.trim()) {
            const q = searchQuery.trim().toLowerCase().normalize('NFD').replace(/[\u0300-\u036f]/g, '');
            list = list.filter(e => {
                const name = e.fullName.toLowerCase().normalize('NFD').replace(/[\u0300-\u036f]/g, '');
                return name.includes(q);
            });
        }
        return [...list].sort((a, b) => a.fullName.localeCompare(b.fullName, 'pt-AO'));
    }, [allEmployees, filterPlant, filterDepartment, filterCompany, employeesWithStatus, searchQuery]);

    const totalPages = Math.ceil(totalEmployees.length / pageSize);
    const employees = useMemo(() => {
        const start = (currentPage - 1) * pageSize;
        return totalEmployees.slice(start, start + pageSize);
    }, [totalEmployees, currentPage]);

    const clearFilters = () => {
        setFilterPlant('');
        setFilterDepartment('');
        setFilterCompany('');
        setFilterStatus('');
        setSearchQuery('');
        setCurrentPage(1);
    };

    // ─── Render ───

    return (
        <div className="att-root">
            {/* Header */}
            <div className="att-header">
                <div>
                    <h2 className="att-header__title">{scope.title}</h2>
                    <p className="att-header__subtitle">{scope.subtitle}</p>
                </div>

                <div className="att-controls">
                    <div className="att-view-toggle">
                        <button
                            className={`att-view-toggle__btn ${viewMode === 'month' ? 'att-view-toggle__btn--active' : ''}`}
                            onClick={() => setViewMode('month')}
                        >
                            <Calendar size={14} /> Mês
                        </button>
                        <button
                            className={`att-view-toggle__btn ${viewMode === 'week' ? 'att-view-toggle__btn--active' : ''}`}
                            onClick={() => setViewMode('week')}
                        >
                            <LayoutGrid size={14} /> Semana
                        </button>
                    </div>

                    <div className="att-nav">
                        <button className="att-nav__btn" onClick={navPrev} title={viewMode === 'week' ? 'Semana anterior' : 'Mês anterior'}>
                            <ChevronLeft size={18} />
                        </button>
                        <span className="att-nav__label">
                            {getNavLabel()}
                            {weekBadge && <span className="att-week-badge">{weekBadge}</span>}
                        </span>
                        <button className="att-nav__btn" onClick={navNext} title={viewMode === 'week' ? 'Próxima semana' : 'Próximo mês'}>
                            <ChevronRight size={18} />
                        </button>
                    </div>
                    <button className="att-nav__today" onClick={navToday}>Hoje</button>

                    <div className="att-refresh-group">
                        <button
                            className="att-refresh-group__btn"
                            onClick={handleForceRefresh}
                            disabled={loading}
                            title="Forçar actualização dos dados do Innux"
                        >
                            <RefreshCw size={14} className={loading ? 'att-loading__icon' : ''} />
                            Atualizar
                        </button>
                        {lastUpdatedAt && (
                            <span className="att-refresh-group__ts">
                                {formatTimestamp(lastUpdatedAt)}
                            </span>
                        )}
                    </div>

                    {totalPages > 1 && (
                        <div className="att-pagination">
                            <button 
                                className="att-pagination__btn" 
                                onClick={() => setCurrentPage(p => Math.max(1, p - 1))}
                                disabled={currentPage === 1}
                            >
                                <ChevronLeft size={16} />
                            </button>
                            <span className="att-pagination__info">
                                {currentPage} / {totalPages}
                            </span>
                            <button 
                                className="att-pagination__btn" 
                                onClick={() => setCurrentPage(p => Math.min(totalPages, p + 1))}
                                disabled={currentPage === totalPages}
                            >
                                <ChevronRight size={16} />
                            </button>
                        </div>
                    )}

                    <button 
                        className="att-help-btn"
                        onClick={() => setIsHelpModalOpen(true)}
                        title="Ajuda e Legenda"
                    >
                        <HelpCircle size={18} />
                    </button>
                </div>
            </div>

            {/* Filters */}
            <div className="att-filters">
                <div className="att-filters__icon"><Filter size={14} /></div>

                <div className="att-filters__search">
                    <Search size={14} className="att-filters__search-icon" />
                    <input
                        type="text"
                        className="att-filters__search-input"
                        placeholder="Pesquisar funcionário..."
                        value={searchQuery}
                        onChange={e => { setSearchQuery(e.target.value); setCurrentPage(1); }}
                    />
                </div>

                    {filterOptions.companies.length > 1 && (
                        <select
                            className="att-filters__select"
                            value={filterCompany}
                            onChange={e => { setFilterCompany(e.target.value); setCurrentPage(1); }}
                        >
                            <option value="">Todas Empresas</option>
                            {filterOptions.companies.map(c => <option key={c} value={c}>{c}</option>)}
                        </select>
                    )}

                    {filterOptions.plants.length > 1 && (
                        <select
                            className="att-filters__select"
                            value={filterPlant}
                            onChange={e => { setFilterPlant(e.target.value); setCurrentPage(1); }}
                        >
                            <option value="">Todas Plantas</option>
                            {filterOptions.plants.map(p => <option key={p} value={p}>{p}</option>)}
                        </select>
                    )}

                    {filterOptions.departments.length > 1 && (
                        <select
                            className="att-filters__select"
                            value={filterDepartment}
                            onChange={e => { setFilterDepartment(e.target.value); setCurrentPage(1); }}
                        >
                            <option value="">Todos Departamentos</option>
                            {filterOptions.departments.map(d => <option key={d} value={d}>{d}</option>)}
                        </select>
                    )}

                    {filterOptions.statuses.length > 1 && (
                        <select
                            className="att-filters__select"
                            value={filterStatus}
                            onChange={e => { setFilterStatus(e.target.value); setCurrentPage(1); }}
                        >
                            <option value="">Todos Status</option>
                            {filterOptions.statuses.map(s => <option key={s} value={s}>{getStatusConfig(s).label}</option>)}
                        </select>
                    )}

                    {hasActiveFilters && (
                        <button className="att-filters__clear" onClick={clearFilters} title="Limpar filtros">
                            <X size={14} /> Limpar
                        </button>
                    )}
                </div>

            {/* Grid */}
            {loading ? (
                <div className="att-loading">
                    <RefreshCw size={24} color="#94a3b8" className="att-loading__icon" />
                    <div className="att-loading__text">A carregar presenças...</div>
                </div>
            ) : error ? (
                <div className="att-error">
                    <div className="att-error__text">{error}</div>
                    <button className="att-error__retry" onClick={() => loadCalendar()}>Tentar novamente</button>
                </div>
            ) : employees.length > 0 ? (
                <div className="att-grid-shell">
                    <div
                        ref={scrollRef}
                        className={`att-scroll-viewport${isScrolled ? ' att-scroll-viewport--scrolled' : ''}${isScrolledY ? ' att-scroll-viewport--scrolled-y' : ''}`}
                    >
                        <table className="att-table" style={{ minWidth: `${280 + days.length * dayColWidth}px` }}>
                            <colgroup>
                                <col style={{ width: '280px' }} />
                                {days.map(d => (
                                    <col key={formatQueryDate(d)} style={{ width: `${dayColWidth}px` }} />
                                ))}
                            </colgroup>
                            <thead>
                                <tr>
                                    <th className="att-frozen-col">
                                        <div className="att-frozen-col__header">
                                            <span className="att-frozen-col__label">Funcionário</span>
                                            {totalEmployees.length > 0 && (
                                                <span className="att-frozen-col__count">({totalEmployees.length})</span>
                                            )}
                                        </div>
                                    </th>
                                    {days.map(d => {
                                        const todayClass = isSameDay(d, today) ? ' att-day-header--today' : '';
                                        const weekendClass = isWeekendDay(d) ? ' att-day-header--weekend' : '';
                                        return (
                                            <th key={formatQueryDate(d)} className={`att-day-header${weekendClass}${todayClass}`}>
                                                <span className="att-day-header__weekday">
                                                    {d.toLocaleString('pt-AO', { weekday: 'narrow' })}
                                                </span>
                                                <span className="att-day-header__number">{d.getDate()}</span>
                                            </th>
                                        );
                                    })}
                                </tr>
                            </thead>
                            <tbody>
                                {employees.map(emp => (
                                    <tr key={emp.innuxEmployeeId}>
                                        <td className="att-frozen-col">
                                            <div className="att-employee">
                                                <span className="att-employee__name" title={emp.fullName}>{emp.fullName}</span>
                                                <span className="att-employee__dept">{emp.department}</span>
                                            </div>
                                        </td>
                                        {days.map((d, dayIdx) => {
                                            const dateKey = formatQueryDate(d);
                                            const record = attendanceLookup.get(`${emp.innuxEmployeeId}_${dateKey}`);
                                            const weekend = isWeekendDay(d);
                                            const isCurrentDay = isSameDay(d, today);
                                            const todayBorder = isCurrentDay ? ' att-day-cell--today-border' : '';
                                            const restDay = record?.isRestDay || false;

                                            // Overnight connector: start marker on this day
                                            const isOvernightStart = !!(record?.isOvernightShift && !record.isRestDay);
                                            // Overnight connector: end marker (previous day was overnight)
                                            const prevDateKey = dayIdx > 0 ? formatQueryDate(days[dayIdx - 1]) : null;
                                            const prevRecord = prevDateKey ? attendanceLookup.get(`${emp.innuxEmployeeId}_${prevDateKey}`) : null;
                                            const isOvernightEnd = !!(prevRecord?.isOvernightShift && !prevRecord.isRestDay);

                                            let cellClass = 'att-day-cell';
                                            if (weekend) cellClass += ' att-day-cell--weekend';
                                            if (isCurrentDay && !record) cellClass += ' att-day-cell--today';
                                            cellClass += todayBorder;
                                            if (restDay) cellClass += ' att-day-cell--rest';
                                            if (isOvernightStart) cellClass += ' att-day-cell--overnight-start';
                                            if (isOvernightEnd) cellClass += ' att-day-cell--overnight-end';

                                            // Tooltip
                                            let cellTitle = record ? getStatusConfig(record.attendanceStatus).label : '';
                                            if (isOvernightStart && record?.scheduleStartTime && record?.scheduleEndTime) {
                                                cellTitle += ` | Turno noturno: ${record.scheduleStartTime} → ${record.scheduleEndTime} (+1 dia)`;
                                            }

                                            return (
                                                <td
                                                    key={formatQueryDate(d)}
                                                    className={cellClass}
                                                    onClick={() => handleCellClick(emp, d)}
                                                    title={cellTitle}
                                                >
                                                    {isCurrentDay && <div className="att-today-indicator" />}
                                                    {record && renderStatusCell(record.attendanceStatus)}
                                                    {record?.scheduleSigla && (
                                                        <span className="att-cell-sigla">{record.scheduleSigla}</span>
                                                    )}
                                                </td>
                                            );
                                        })}
                                    </tr>
                                ))}
                            </tbody>
                        </table>
                    </div>

                    <div className={`att-scroll-hint ${showScrollHint ? 'att-scroll-hint--visible' : ''}`} />


                    {/* Legend */}
                    <div className="att-legend">
                        {renderLegendItem('Present')}
                        {renderLegendItem('Absent')}
                        {renderLegendItem('JustifiedAbsence')}
                        {renderLegendItem('Vacation')}
                        {renderLegendItem('Holiday')}
                        {renderLegendItem('DayOff')}
                        {renderLegendItem('Anomaly')}
                        <div className="att-legend__item">
                            <div className="att-legend__icon att-legend__icon--overnight">
                                <Moon size={12} />
                            </div>
                            <span className="att-legend__text">Turno Noturno</span>
                        </div>
                        <span className="att-legend__scope">{getScopeDescription()}</span>
                    </div>
                </div>
            ) : (
                <div className="att-empty">
                    {calendarData?.scopeType === 'self'
                        ? 'Nenhum registo de presença encontrado para a sua conta.'
                        : 'Nenhum funcionário mapeado no seu escopo de hierarquia.'}
                </div>
            )}

            {/* Legend / Help Modal */}
            <GuideModal
                isOpen={isHelpModalOpen}
                onClose={() => setIsHelpModalOpen(false)}
                title="Guia de Assiduidade"
                subtitle="Entenda como os dados do Innux são apresentados no Portal Gerencial."
            >
                <GuideModalSection
                    title="Status e Legenda"
                    icon={<Info size={20} />}
                    iconBgColor="#eff6ff"
                    iconColor="#2563eb"
                >
                    <div className="att-guide-list">
                        {Object.entries(STATUS_VISUAL_MAP).map(([key, vis]) => (
                            <div className="att-guide-item" key={key}>
                                <div className={`att-guide-icon att-guide-icon--${vis.cssClass}`}>
                                    {vis.renderIcon(14)}
                                </div>
                                <div><strong>{vis.label}:</strong> {vis.description}</div>
                            </div>
                        ))}
                        <div className="att-guide-item">
                            <div className="att-guide-icon att-guide-icon--overnight">
                                <Moon size={14} />
                            </div>
                            <div><strong>Turno Noturno:</strong> Turno com período que atravessa a meia-noite.</div>
                        </div>
                    </div>
                </GuideModalSection>

                <GuideModalSection
                    title="Métricas de Tempo"
                    icon={<Clock size={20} />}
                    iconBgColor="#fef2f2"
                    iconColor="#dc2626"
                >
                    <div className="att-guide-text">
                        <p><strong>Esperado:</strong> O tempo total planeado para o turno (Ex: 08:00 às 17:30 = 9h totais).</p>
                        <p><strong>Ausência:</strong> Tempo que o funcionário deixou de trabalhar sem justificativa.</p>
                        <p><strong>Saldo:</strong> Diferença entre o tempo trabalhado e o planeado. 
                           <span style={{ color: '#dc2626', fontWeight: 600 }}> Vermelho</span> indica horas negativas em falta.</p>
                        <p><strong>Marcações:</strong> Contagem total de vezes que o funcionário passou o cartão no terminal.</p>
                    </div>
                </GuideModalSection>

                <GuideModalSection
                    title="Marcações Brutas"
                    icon={<UserCheck size={20} />}
                    iconBgColor="#f0fdf4"
                    iconColor="#16a34a"
                >
                    <p className="att-guide-text">
                        As marcações brutas são os registos reais extraídos dos terminais. 
                        Quando aparece a tag <span className="att-punch-auto" style={{ float: 'none', display: 'inline' }}>Auto</span>, 
                        significa que o sistema Innux gerou essa marcação automaticamente para fechar um par de entrada/saída que ficou incompleto.
                    </p>
                </GuideModalSection>
            </GuideModal>

            {/* Detail Drawer */}
            {drawerOpen && (
                <>
                    <div className="att-drawer-overlay" onClick={closeDrawer} />
                    <div className="att-drawer">
                        <div className="att-drawer__header">
                            <div style={{ flex: 1, minWidth: 0 }}>
                                <h3 className="att-drawer__title">
                                    {drawerEmployee?.fullName || 'Detalhe'}
                                </h3>
                                <div className="att-drawer__nav-row">
                                    <button
                                        className="att-drawer__nav-btn"
                                        onClick={() => navigateDrawer('prev')}
                                        disabled={!canDrawerPrev}
                                        title="Dia anterior"
                                    >
                                        <ChevronLeft size={16} />
                                    </button>
                                    <p className="att-drawer__subtitle">
                                        {drawerDate?.toLocaleDateString('pt-AO', { weekday: 'long', day: 'numeric', month: 'long', year: 'numeric' })}
                                    </p>
                                    <button
                                        className="att-drawer__nav-btn"
                                        onClick={() => navigateDrawer('next')}
                                        disabled={!canDrawerNext}
                                        title="Dia seguinte"
                                    >
                                        <ChevronRight size={16} />
                                    </button>
                                </div>
                            </div>
                            <button className="att-drawer__close" onClick={closeDrawer}>
                                <X size={20} />
                            </button>
                        </div>

                        <div className="att-drawer__body">
                            {drawerLoading ? (
                                <div className="att-drawer__loading">
                                    <RefreshCw size={18} className="att-loading__icon" />
                                    <span>A carregar...</span>
                                </div>
                            ) : drawerError ? (
                                <div className="att-drawer__error">{drawerError}</div>
                            ) : drawerDetail ? (
                                <DrawerContent detail={drawerDetail} />
                            ) : (
                                <div className="att-detail-empty">
                                    Sem dados de presença para este dia.
                                </div>
                            )}
                        </div>
                    </div>
                </>
            )}
        </div>
    );
}

// ─── Drawer Content (sub-component) ───

function DrawerContent({ detail }: { detail: DayDetail }) {
    const { summary, rawPunches, periods } = detail;
    const [debugOpen, setDebugOpen] = useState(false);

    // Detect processed-but-no-raw scenario
    const hasProcessedButNoRaw = summary.punchCount > 0 && rawPunches.length === 0;
    // Detect absence-with-raw-terminal-events contradiction
    const hasAbsenceWithRawPunches = summary.attendanceStatus === 'Anomaly'
        && summary.absenceMinutes > 0
        && rawPunches.length > 0
        && !summary.firstEntry && !summary.firstExit
        && summary.punchCount === 0;
    const cfg = getStatusConfig(summary.attendanceStatus);

    // Build overnight schedule label
    const overnightLabel = summary.isOvernightShift && summary.scheduleStartTime && summary.scheduleEndTime
        ? `${summary.scheduleStartTime} → ${summary.scheduleEndTime} (+1 dia)`
        : null;

    return (
        <>
            {/* Overnight badge */}
            {summary.isOvernightShift && !summary.isRestDay && (
                <div className="att-overnight-badge">
                    <Moon size={14} />
                    <span>Turno Noturno</span>
                    {overnightLabel && <span className="att-overnight-badge__time">{overnightLabel}</span>}
                </div>
            )}

            {/* Summary */}
            <div className="att-detail-section">
                <div className="att-detail-section__title">Resumo</div>
                <div className={`att-status-badge att-status-badge--${cfg.cssClass}`}>
                    {cfg.label}
                </div>
                <div className="att-summary-grid">
                    <SummaryItem label="Entrada" value={summary.firstEntry || '—'} />
                    <SummaryItem label="Saída" value={summary.firstExit || '—'} />
                    <SummaryItem label="Esperado" value={formatMinutes(summary.expectedMinutes)} />
                    <SummaryItem label="Ausência" value={summary.absenceMinutes > 0 ? formatMinutes(summary.absenceMinutes) : '—'} />
                    <SummaryItem label="Aus. Justificada" value={summary.justifiedAbsenceMinutes > 0 ? formatMinutes(summary.justifiedAbsenceMinutes) : '—'} />
                    <SummaryItem label="Saldo" value={formatMinutes(summary.balanceMinutes)} provisional />
                    <SummaryItem label="Marcações (processado)" value={String(summary.punchCount)} />
                    {rawPunches.length !== summary.punchCount && (
                        <SummaryItem label="Marcações (terminal)" value={String(rawPunches.length)} />
                    )}
                    {(summary.basicWorkedMinutes > 0 || summary.overtimeMinutes > 0) && (
                        <>
                            <SummaryItem label="Básico" value={formatMinutes(summary.basicWorkedMinutes)} />
                            <SummaryItem label="Extra" value={summary.overtimeMinutes > 0 ? formatMinutes(summary.overtimeMinutes) : '—'} />
                            <SummaryItem label="Total Trab." value={formatMinutes(summary.totalWorkedMinutes)} />
                        </>
                    )}
                    <SummaryItem label="Validado" value={summary.isValidated ? 'Sim' : 'Não'} />
                    {summary.scheduleDescription && (
                        <SummaryItem label="Horário" value={`${summary.scheduleDescription}${summary.scheduleSigla ? ` (${summary.scheduleSigla})` : ''}`} />
                    )}
                    {summary.isRestDay && (
                        <SummaryItem label="Tipo Dia" value="Dia de Descanso" />
                    )}
                    {summary.anomalyDescription && (
                        <SummaryItem label="Anomalia" value={summary.anomalyDescription} />
                    )}
                    {summary.justification && (
                        <SummaryItem label="Justificação" value={summary.justification} />
                    )}
                </div>
                {detail.workPlanDescription && (
                    <div className="att-summary-grid" style={{ marginTop: 8 }}>
                        <SummaryItem label="Plano de Trabalho" value={detail.workPlanDescription} />
                    </div>
                )}
            </div>

            {/* Periods */}
            <div className="att-detail-section">
                <div className="att-detail-section__title">Períodos</div>
                {periods.length > 0 ? (
                    periods.map((p, i) => (
                        <div key={i} className={`att-period ${p.isDispensed ? 'att-period--dispensed' : ''}`}>
                            <div className="att-period__time">
                                {p.startTime || '??:??'} — {p.endTime || '??:??'}
                            </div>
                            <div className="att-period__info">
                                {p.workCode && <span className="att-period__code">{p.workCode}</span>}
                                {p.workDescription && <span className="att-period__desc">{p.workDescription}</span>}
                                {p.absenceCode && <span className="att-period__code" style={{ color: '#d97706' }}>{p.absenceCode}</span>}
                                {p.absenceDescription && <span className="att-period__desc">{p.absenceDescription}</span>}
                                {p.isDispensed && <span className="att-period__desc" style={{ fontStyle: 'italic' }}>Dispensado</span>}
                            </div>
                        </div>
                    ))
                ) : (
                    <div className="att-detail-empty">Sem períodos registados.</div>
                )}
            </div>

            {/* Info banner: processed but no raw punches (anomaly explanation) */}
            {hasProcessedButNoRaw && (
                <div className="att-info-banner att-info-banner--anomaly">
                    <ShieldAlert size={14} className="att-info-banner__icon" />
                    <span>
                        <strong>Anomalia:</strong> Dia processado pelo Innux sem marcações brutas de terminal
                        disponíveis. Este registo pode ter sido gerado automaticamente por rotação de escala
                        (Escala) ou validação manual no Innux. Recomenda-se revisão pelo R.H.
                    </span>
                </div>
            )}

            {/* Info banner: absence declared but raw terminal events exist (not recognised as Entry/Exit) */}
            {hasAbsenceWithRawPunches && (
                <div className="att-info-banner att-info-banner--anomaly">
                    <ShieldAlert size={14} className="att-info-banner__icon" />
                    <span>
                        <strong>Anomalia:</strong> Existem marcações brutas no terminal, mas o Innux processou o período como
                        Falta Injustificada. Verifique se as marcações foram feitas com código/direção não reconhecida
                        ou se há necessidade de correção pelo R.H.
                    </span>
                </div>
            )}

            {/* Raw Punches */}
            <div className="att-detail-section">
                <div className="att-detail-section__title">
                    <span style={{ display: 'flex', alignItems: 'center', gap: 6 }}>
                        <Clock size={12} /> Marcações Brutas de Terminal
                    </span>
                </div>
                {rawPunches.length > 0 ? (
                    <table className="att-punches-table">
                        <thead>
                            <tr>
                                <th>Hora</th>
                                <th>Direção / Código</th>
                                <th>Terminal</th>
                                <th></th>
                            </tr>
                        </thead>
                        <tbody>
                            {rawPunches.map((p, i) => (
                                <tr key={i}>
                                    <td style={{ fontWeight: 700 }}>{p.time}</td>
                                    <td>{p.directionLabel || p.direction}</td>
                                    <td>{p.terminalName || '—'}</td>
                                    <td>{p.isAutoGenerated && <span className="att-punch-auto">Auto</span>}</td>
                                </tr>
                            ))}
                        </tbody>
                    </table>
                ) : (
                    <div className="att-detail-empty">
                        {hasProcessedButNoRaw
                            ? 'Nenhum registo de terminal disponível — marcação consolidada pelo Innux.'
                            : 'Sem marcações brutas.'}
                    </div>
                )}
            </div>

            {/* Debug / Technical Section */}
            <div className="att-detail-section att-debug-section">
                <button
                    className="att-debug-toggle"
                    onClick={() => setDebugOpen(prev => !prev)}
                    type="button"
                >
                    <span style={{ display: 'flex', alignItems: 'center', gap: 6 }}>
                        <Database size={12} /> Dados Técnicos Innux
                    </span>
                    {debugOpen ? <ChevronUp size={14} /> : <ChevronDown size={14} />}
                </button>
                {debugOpen && (
                    <div className="att-debug-content">
                        <div className="att-debug-grid">
                            <span className="att-debug-label">Alteracoes.Marcacao</span>
                            <span className="att-debug-value">{detail.debugProcessedPunchCount}</span>

                            <span className="att-debug-label">TerminaisMarcacoes (live)</span>
                            <span className="att-debug-value">{detail.debugRawTerminalPunchCount}</span>

                            <span className="att-debug-label">Validado</span>
                            <span className="att-debug-value">{detail.debugIsValidated ? 'Sim' : 'Não'}</span>

                            <span className="att-debug-label">Horário (Código)</span>
                            <span className="att-debug-value">{detail.debugScheduleCode || '—'}</span>

                            <span className="att-debug-label">Horário (Descrição)</span>
                            <span className="att-debug-value">{detail.debugScheduleDescription || '—'}</span>

                            <span className="att-debug-label">Fonte do Status</span>
                            <span className="att-debug-value">{detail.debugStatusSource || '—'}</span>
                        </div>
                    </div>
                )}
            </div>
        </>
    );
}

function SummaryItem({ label, value, provisional }: { label: string; value: string; provisional?: boolean }) {
    return (
        <div className="att-summary-item">
            <span className="att-summary-item__label">{label}</span>
            <span className={`att-summary-item__value ${provisional ? 'att-summary-item__value--provisional' : ''}`}>
                {value}
                {provisional && <AlertTriangle size={10} style={{ marginLeft: 4, verticalAlign: 'middle' }} />}
            </span>
        </div>
    );
}
